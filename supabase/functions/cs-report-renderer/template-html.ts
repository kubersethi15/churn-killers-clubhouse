// deno-lint-ignore no-explicit-any
function buildSnapshotPage(report: any, customer: string, date: string, n: number): string {
  const snap = report.executive_snapshot;
  const threat = snap?.primary_threat || "unknown";
  const threatLabel = formatCallType(threat);
  const badgeClass = threatBadgeClass(threat);

  const takeawaysHtml = (snap?.top_3_takeaways || [])
    .map(
      // deno-lint-ignore no-explicit-any
      (t: any, idx: number) => `
    <div class="takeaway-item no-break">
      <div class="takeaway-num">${idx + 1}</div>
      <div>
        <div class="takeaway-text">${esc(t.takeaway)}</div>
        <div class="takeaway-confidence">${confidenceDot(t.confidence)} ${esc(t.confidence?.charAt(0).toUpperCase() + t.confidence?.slice(1))} confidence</div>
      </div>
    </div>`,
    )
    .join("\n");

  return `<div class="page">
  ${pageHeader(customer, date)}

  ${sectionHeader("ES", "var(--brand-accent-light)", "var(--brand-accent)", "Executive Snapshot", "Key indicators at a glance", `<span class="badge ${badgeClass}">${esc(threatLabel)} Risk</span>`)}

  <div class="snapshot-card">
    <div class="snapshot-threat-row">
      <span class="snapshot-threat-badge">Primary Threat: ${esc(threatLabel)}</span>
      ${snap?.secondary_threat ? `<span class="snapshot-threat-badge" style="opacity:0.6;">Secondary: ${esc(formatCallType(snap.secondary_threat))}</span>` : `<span class="snapshot-threat-badge" style="opacity:0.6;">Secondary: None</span>`}
    </div>
    <div class="snapshot-one-liner">${esc(snap?.one_liner || "")}</div>
  </div>

  <div class="takeaway-grid">${takeawaysHtml}</div>

  <div class="strategic-truth" style="margin-top:24px;">
    <div class="strategic-truth-label">Strategic Truth</div>
    <div class="strategic-truth-text">${esc(snap?.one_liner || "")}</div>
  </div>

  ${pageFooter(n)}
</div>`;
}


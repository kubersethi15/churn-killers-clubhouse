function sectionHeader(
  abbr: string,
  bg: string,
  fg: string,
  title: string,
  subtitle: string,
  badge?: string,
): string {
  return `<div class="section-header">
    <div class="section-icon" style="background:${bg};color:${fg};">${abbr}</div>
    <div>
      <div class="section-title">${esc(title)}</div>
      ${subtitle ? `<div class="section-subtitle">${esc(subtitle)}</div>` : ""}
    </div>
    ${badge ? `<div style="margin-left:auto;">${badge}</div>` : ""}
  </div>`;
}


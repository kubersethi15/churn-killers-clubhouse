// deno-lint-ignore no-explicit-any
function buildStakeholderExpansionPage(report: any, vis: Record<string, boolean>, si: Record<string, boolean>, customer: string, date: string, n: number): string {
  const stakeholders = report.stakeholder_power_map?.stakeholders || [];
  const summary = report.stakeholder_power_map?.summary || {};
  const expansion = report.expansion_readiness;
  const showStakeholders = shouldShow(vis, si, "stakeholder_power_map") && stakeholders.length > 0;
  const showExpansion = expansion != null;

  let stakeholdersHtml = "";
  if (showStakeholders) {
    const powerDist = summary.power_distribution || {};
    const stanceDist = summary.stance_distribution || {};
    const summaryText = `${stakeholders.length} stakeholders identified · Power: ${powerDist.high || 0} high, ${powerDist.medium || 0} medium, ${powerDist.low || 0} low · Stance: ${stanceDist.supportive || 0} supportive, ${stanceDist.skeptical || 0} skeptical, ${stanceDist.neutral || 0} neutral`;

    const cardsHtml = stakeholders
      .map(
        // deno-lint-ignore no-explicit-any
        (s: any) => {
          const roleParts = (s.name_or_title || "").split(" — ");
          const name = roleParts[0] || s.name_or_title;
          const role = roleParts[1] || s.role_in_decision || "";
          const motivation = s.motivation_or_pressure || "";
          const decisionRole = formatCallType(s.role_in_decision || "unknown");

          return `
      <div class="stakeholder-card no-break">
        <div class="stakeholder-identity">
          <div class="stakeholder-name">${esc(name)}</div>
          ${role ? `<div class="stakeholder-role">${esc(role)}</div>` : ""}
        </div>
        <div class="stakeholder-meta-grid">
          <div class="stakeholder-meta-item">
            <div class="stakeholder-meta-label">Power</div>
            <div class="stakeholder-meta-value"><span class="badge ${powerBadgeClass(s.power)}">${esc(formatCallType(s.power || ""))}</span></div>
          </div>
          <div class="stakeholder-meta-item">
            <div class="stakeholder-meta-label">Stance</div>
            <div class="stakeholder-meta-value"><span class="badge ${stanceBadgeClass(s.stance)}">${esc(formatCallType(s.stance || ""))}</span></div>
          </div>
          <div class="stakeholder-meta-item">
            <div class="stakeholder-meta-label">Decision Role</div>
            <div class="stakeholder-meta-value">${esc(decisionRole)}</div>
          </div>
          ${motivation ? `<div class="stakeholder-motivation"><strong>Motivation:</strong> ${esc(motivation)}</div>` : ""}
        </div>
      </div>`;
        },
      )
      .join("\n");

    stakeholdersHtml = `
    ${sectionHeader("SM", "#F0E8FE", "#6B21A8", "Stakeholder Power Map", summaryText)}
    <div class="stakeholder-grid">${cardsHtml}</div>`;
  }

  let expansionHtml = "";
  if (showExpansion) {
    const stages = ["no_signal", "interest", "evaluation", "negotiation", "commitment"];
    const stageLabels: Record<string, string> = {
      no_signal: "No Signal",
      interest: "Interest",
      evaluation: "Evaluation",
      negotiation: "Negotiation",
      commitment: "Commitment",
    };
    const activeStage = (expansion.stage || "").toLowerCase().replace(/\s+/g, "_");
    const activeIndex = stages.indexOf(activeStage);

    const pipelineHtml = stages
      .map((stage, i) => {
        const cls = i < activeIndex ? "passed" : i === activeIndex ? "active" : "";
        return `<div class="pipeline-stage ${cls}">${stageLabels[stage]}</div>`;
      })
      .join("");

    const gateConditions = (expansion.gate_conditions || [])
      .map(
        (g: string) =>
          `<div class="gate-item no-break"><span class="gate-icon">&bull;</span> ${esc(g)}</div>`,
      )
      .join("\n");

    const blockers = (expansion.blockers || [])
      .map(
        (b: string) =>
          `<div class="gate-item no-break" style="border-left-color:var(--brand-danger);"><span class="gate-icon" style="color:var(--brand-danger);">&bull;</span> ${esc(b)}</div>`,
      )
      .join("\n");

    expansionHtml = `
    <div style="height:28px;"></div>
    ${sectionHeader("ER", "var(--brand-accent-light)", "var(--brand-accent)", "Expansion Readiness", `${esc(expansion.stage || "Unknown")} stage`)}
    <div class="expansion-pipeline">${pipelineHtml}</div>
    ${gateConditions ? `<div class="sub-section-label">Gate Conditions</div><div class="gate-list">${gateConditions}</div>` : ""}
    ${blockers ? `<div class="sub-section-label" style="margin-top:12px;">Blockers</div><div class="gate-list">${blockers}</div>` : ""}`;
  }

  return `<div class="page">
  ${pageHeader(customer, date)}
  ${stakeholdersHtml}
  ${expansionHtml}
  ${pageFooter(n)}
</div>`;
}


interface BuildParams {
  // deno-lint-ignore no-explicit-any
  report: any;
  visibility: Record<string, boolean>;
  title: string;
  finalizedAt: string;
}

export function buildReportHtml(params: BuildParams): string {
  const { report, visibility, title, finalizedAt } = params;
  const si = report.section_included || {};
  const dateStr = formatDate(
    finalizedAt || report.meta?.generated_at_iso || new Date().toISOString(),
  );
  const customer = title || "Analysis Report";
  const callType = formatCallType(report.meta?.call_type || "unknown");

  let pageNum = 1;
  const pages: string[] = [];

  // Page 1: Cover (always)
  pages.push(buildCoverPage(customer, callType, dateStr, pageNum++, report));

  // Page 2: Executive Snapshot (always)
  if (report.executive_snapshot) {
    pages.push(buildSnapshotPage(report, customer, dateStr, pageNum++));
  }

  // Page 3: Evidence Facts (conditional)
  if (
    shouldShow(visibility, si, "evidence_backed_facts") &&
    report.evidence_backed_facts?.length > 0
  ) {
    pages.push(buildFactsPage(report.evidence_backed_facts, customer, dateStr, pageNum++));
  }

  // Page 4: Risks + Actions (always)
  pages.push(buildRisksActionsPage(report, customer, dateStr, pageNum++));

  // Page 5: Stakeholders + Expansion (conditional)
  const showStakeholders =
    shouldShow(visibility, si, "stakeholder_power_map") &&
    report.stakeholder_power_map?.stakeholders?.length > 0;
  const showExpansion = report.expansion_readiness != null;
  if (showStakeholders || showExpansion) {
    pages.push(
      buildStakeholderExpansionPage(report, visibility, si, customer, dateStr, pageNum++),
    );
  }

  // Page 6: Procurement + Incident (conditional)
  const showProcurement =
    shouldShow(visibility, si, "procurement_and_timeline") &&
    report.procurement_and_timeline;
  const showIncident =
    shouldShow(visibility, si, "incident_impact") && report.incident_impact;
  if (showProcurement || showIncident) {
    pages.push(
      buildProcurementIncidentPage(report, visibility, si, customer, dateStr, pageNum++),
    );
  }

  // Page 7: Gaps + Effectiveness (conditional)
  const showConvGaps =
    shouldShow(visibility, si, "conversational_gaps") &&
    report.conversational_gaps?.length > 0;
  const showValueGaps =
    shouldShow(visibility, si, "value_narrative_gaps") &&
    report.value_narrative_gaps?.length > 0;
  const showCsRep =
    shouldShow(visibility, si, "cs_rep_effectiveness") &&
    report.cs_rep_effectiveness;
  if (showConvGaps || showValueGaps || showCsRep) {
    pages.push(
      buildGapsEffectivenessPage(report, visibility, si, customer, dateStr, pageNum++),
    );
  }

  const css = getEnterpriseCss();

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>${esc(customer)} &mdash; CS Intelligence Report</title>
<link rel="preconnect" href="https://fonts.googleapis.com">
<link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
<link href="https://fonts.googleapis.com/css2?family=DM+Sans:wght@400;500;600;700&display=swap" rel="stylesheet">
<style>${css}</style>
</head>
<body>
${pages.join("\n")}
</body>
</html>`;
}

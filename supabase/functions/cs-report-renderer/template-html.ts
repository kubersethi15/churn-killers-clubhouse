// ─── Helpers ────────────────────────────────────────────────────────────────

function esc(s: unknown): string {
  const str = String(s ?? "");
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function formatDate(iso: string): string {
  try {
    return new Date(iso).toLocaleDateString("en-GB", {
      day: "numeric",
      month: "long",
      year: "numeric",
    });
  } catch {
    return iso;
  }
}

function formatCallType(raw: string): string {
  return raw
    .replace(/_/g, " ")
    .replace(/\b\w/g, (c) => c.toUpperCase());
}

function shouldShow(
  vis: Record<string, boolean>,
  si: Record<string, boolean>,
  key: string,
): boolean {
  const alwaysVisible = [
    "executive_snapshot",
    "risks_and_threats",
    "action_plan_14_days",
  ];
  if (alwaysVisible.includes(key)) return true;
  return si[key] !== false && vis[key] !== false;
}

function confidenceDot(level: string): string {
  const cls =
    level === "high"
      ? "confidence-high"
      : level === "medium"
        ? "confidence-medium"
        : "confidence-low";
  return `<span class="confidence-dot ${cls}"></span>`;
}

function confidenceBadge(level: string): string {
  const label = (level || "unknown").charAt(0).toUpperCase() + (level || "unknown").slice(1);
  const cls =
    level === "high"
      ? "badge-success"
      : level === "medium"
        ? "badge-warning"
        : "badge-neutral";
  return `<span class="badge ${cls}" style="white-space:nowrap;">${confidenceDot(level)} ${esc(label)}</span>`;
}

function threatBadgeClass(threat: string): string {
  if (["churn", "displacement"].includes(threat)) return "badge-danger";
  if (["delay", "downsell"].includes(threat)) return "badge-warning";
  if (threat === "none") return "badge-success";
  return "badge-neutral";
}

function severityClass(sev: string): string {
  if (sev === "critical") return "sev-critical";
  if (sev === "high") return "sev-high";
  if (sev === "medium") return "sev-medium";
  return "sev-low";
}

function categoryClass(cat: string): string {
  const map: Record<string, string> = {
    adoption: "cat-adoption",
    value: "cat-value",
    incident: "cat-incident",
    budget: "cat-budget",
    stakeholder: "cat-stakeholder",
    political: "cat-political",
  };
  return map[cat?.toLowerCase()] || "cat-adoption";
}

function powerBadgeClass(power: string): string {
  if (power === "high") return "badge-danger";
  if (power === "medium") return "badge-warning";
  return "badge-neutral";
}

function stanceBadgeClass(stance: string): string {
  if (stance === "supportive") return "badge-success";
  if (["skeptical", "resistant"].includes(stance)) return "badge-danger";
  return "badge-neutral";
}

function threatColor(threat: string): string {
  if (["churn", "displacement"].includes(threat)) return "var(--brand-danger)";
  if (["delay", "downsell"].includes(threat)) return "var(--brand-warning)";
  if (threat === "none") return "var(--brand-success)";
  return "var(--brand-neutral)";
}

function sevLabel(sev: string): string {
  if (sev === "critical") return "Crit";
  if (sev === "high") return "High";
  if (sev === "medium") return "Med";
  return "Low";
}

